        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        private static ulong MixFunction1(ulong key)
        {
            key = (key ^ (key >> 31)) * 0x7FB5D329728EA185UL;
            key = (key ^ (key >> 27)) * 0x81DADEF4BC2DD44DUL;
            return key ^ (key >> 33);
        }

